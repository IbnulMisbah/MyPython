# Simple List or Arrey.
fruits = ['apple','mango','kathal','jam','jambura','lichi','tetul','lemon']

# Printing all fruits by for loop. fruits = 8.
for i in range (0 , 8):
    print(fruits[i])

print(10*'#')

# Printing all fruits by while loop. 
i = 0
while i < 8:
    print(fruits[i])
    i = i+1

print(10*'*')

# Printing all fruits by "Python Style". 
for fruits in fruits:
    print(fruits)
