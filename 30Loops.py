# Determining Square of 1 to 10 by for loop.
# Here 11-1=10.So range is (1 , 11).
for i in range (1 , 11):
	print (i,'Square is equal to',i*i)

# Determining Square of 1 to 10 by while loop.
i = 1
while i < 11:
	print(i,'Square is equal to',i*i)
	i = i+1

# Determining Odd number in range of 1 to 100 by for loop.
for i in range (1 , 100 , 2) :
	print(i)

